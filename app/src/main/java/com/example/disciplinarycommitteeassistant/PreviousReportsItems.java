package com.example.disciplinarycommitteeassistant;

public class PreviousReportsItems {
    String title;
    String subtitle;

    public PreviousReportsItems(String title, String subtitle) {
        this.title = title;
        this.subtitle = subtitle;
    }
    public String getTitle() {
        return this.title;
    }

    public String getSubtitle() {
        return subtitle;
    }
}
